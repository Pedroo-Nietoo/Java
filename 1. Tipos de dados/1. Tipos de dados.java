/*
Na linguagem java existem alguns tipos de dados que é preciso aprender para poder programar com facilidade, sejam eles números, caracteres ou outros. São eles:
  
--int: indica um número inteiro que vai de -2.147.483.648 a 2.147.483.647;
--double: indica um número não inteiro;
--byte: tipo de dado mais baixo do Java. Também serve para números (1 byte possui 8 bits) ;  
--char: indica um caractere;
--short: indica um número inteiro que vai de -32.768 a 32.767;
--long:  indica um número inteiro que vai de -9.223.372.036.854.775.808L a 9.223.372.036.854.775.807;
--float: indica um número ponto-flutuante (com partes fracionárias);
--boolean: retorna um valor Verdadeiro ou Falso (true/false);
--String: todos os tipos de dados anteriores. Entretanto, o tipo String não realiza cálculos

Utilizaremos esses tipos de dados para criar variáveis onde os valores que quisermos serão armazenados. Veja um exemplo:

 --int var1 = 1;
Criamos uma variável de nome "var1" do tipo "int" que tem como valor o número 1. Podemos fazer a mesma coisa com outros tipos de dados. Veja:

--double var2 = 0.5;
--boolean var3 = true;
--char letra = "a";
--String user = "@Pedroo-Nietoo";

Pode parecer um pouco confuso no início, então vamos partir para exemplos na prática. 
*/

